function calcularMencao() {
    const sexo = document.getElementById("sexo").value;
    const faixaEtaria = document.getElementById("faixaEtaria").value;
    const corrida = parseInt(document.getElementById("corrida").value);
    const barra = parseInt(document.getElementById("barra").value);
    const abdominal = parseInt(document.getElementById("abdominal").value);
    const flexao = parseInt(document.getElementById("flexao").value);

    if (!sexo || !faixaEtaria || isNaN(corrida) || isNaN(barra) || isNaN(abdominal) || isNaN(flexao)) {
        document.getElementById("resultado").textContent = "Por favor, preencha todos os campos antes de calcular.";
        return;
    }

    let mencao = "Indefinida";
    let somaPontos = 0;

    // Seleciona os índices com base no sexo selecionado
    const indices = sexo === "masculino" ? indicesMasculinos : indicesFemininos;

    // Verifica se há índices para a faixa etária escolhida
    if (!indices[faixaEtaria]) {
        document.getElementById("resultado").textContent = "Faixa etária inválida para o sexo selecionado.";
        return;
    }

    // Função para calcular a pontuação com base nos índices
    function obterPontuacao(indices, corridaNum, barraNum, abdominalNum, flexaoNum) {
        let pontosCorrida = 0, pontosBarra = 0, pontosAbdominal = 0, pontosFlexao = 0;

        for (const indice of indices) {
            if (corridaNum >= indice.corrida[0] && corridaNum <= indice.corrida[1] && pontosCorrida === 0) {
                pontosCorrida = indice.pontos;
            }
            if (Array.isArray(indice.barra)) { 
                if (barraNum >= indice.barra[0] && barraNum <= indice.barra[1] && pontosBarra === 0) {
                    pontosBarra = indice.pontos;
                }
            } else {
                if (barraNum === indice.barra && pontosBarra === 0) {
                    pontosBarra = indice.pontos;
                }
            }
            if (abdominalNum >= indice.abdominal[0] && abdominalNum <= indice.abdominal[1] && pontosAbdominal === 0) {
                pontosAbdominal = indice.pontos;
            }
            if (flexaoNum >= indice.flexao[0] && flexaoNum <= indice.flexao[1] && pontosFlexao === 0) {
                pontosFlexao = indice.pontos;
            }
        }

        return { pontosCorrida, pontosBarra, pontosAbdominal, pontosFlexao };
    }

    const pontosExercicio = obterPontuacao(indices[faixaEtaria], corrida, barra, abdominal, flexao);

    somaPontos = pontosExercicio.pontosCorrida + pontosExercicio.pontosBarra + pontosExercicio.pontosAbdominal + pontosExercicio.pontosFlexao;

    if (somaPontos >= 39 && somaPontos <= 40) {
        mencao = "Excelente";
    } else if (somaPontos >= 36 && somaPontos <= 38) {
        mencao = "Muito Bom";
    } else if (somaPontos >= 30 && somaPontos <= 35) {
        mencao = "Bom";
    } else if (somaPontos >= 23 && somaPontos <= 29) {
        mencao = "Regular";
    } else if (somaPontos <= 22) {
        mencao = "Insuficiente";
    }

    document.getElementById("resultado").innerHTML = `
        <p>Menção: <strong>${mencao}</strong></p>
        <p>Pontuação Total: ${somaPontos}</p>
        <p><strong>Pontuação por Exercício:</strong></p>
        <ul>
            <li>Corrida: ${pontosExercicio.pontosCorrida} pontos</li>
            <li>Barra/Isometria: ${pontosExercicio.pontosBarra} pontos</li>
            <li>Abdominal: ${pontosExercicio.pontosAbdominal} pontos</li>
            <li>Flexão de Braços: ${pontosExercicio.pontosFlexao} pontos</li>
        </ul>
    `;
}
